export default function EvalPromptsPage() {
  return (
    <div className="docs-content">
      <h1>Writing Eval Prompts</h1>
      <p>
        An eval prompt is designed to measure model performance on a specific capability.
        It helps with judging quality against criteria and identifying areas for improvement.
        It is a crucial component of the eval pipeline and prompts should be very specific on what they are measuring.
      </p>

      <h2>3 Key things to include in your eval prompt</h2>
      <h3>1. Clear objective</h3>
      <p>What are you testing?</p>
      <p>Bad Example:</p>
      <pre>
        <code>{` Task: “Summarise this log file”`}</code>
      </pre>
      <p>Good Example:</p>
      <pre>
        <code>{` Task: “Summarise the log file in under 100 words. 
        Include:
        the main error
        the timestamp of first occurrence
        one likely cause
        Do not include any irrelevant log lines.”`}</code>
      </pre>
      <h3>2. Constrained output</h3>
      <p>You want answers that are easy to evaluate, this reduces ambiguity and makes scoring easier
         Think about the following:
      </p>
      <ul>
        <li>Limit length</li>
        <li>Specify format</li>
        <li>Define structure</li>
      </ul>
      <p>Good Example:</p>
      <pre>
        <code>{` Task: “Return your answer as a JSON object with keys: cause, explanation.
        cause: a short phrase (max 5 words)
        explanation: a concise explanation (max 20 words)
        Do not include any additional keys or text outside the JSON.”`}</code>
      </pre>
      <h3>3. Rubric-based scoring</h3>
      <p>
        A rubric-based scoring system is a way to evaluate the quality of a response based on a set of criteria.
      </p>
      <p>Good Example:</p>
      <pre>
        <code>{`"rubric": {
          "correct_facts": {
            "description": "Summary includes the key points (policy introduction, increase in recycling, resident confusion, future campaign)",
            "score_range": "0-2"
          },
          "clarity": {
            "description": "Summary is easy to understand and logically structured",
            "score_range": "0-2"
          },
          "format_compliance": {
            "description": "Under 40 words and no extra commentary",
            "score_range": "0-1"
          }`
        }</code>
      </pre>
      <h2>Common mistakes when writing eval prompts</h2>
      <ul>
        <li>Too vague → Hard to evaluate consistently</li>
        <li>Too open-ended → Outputs vary wildly</li>
        <li>No scoring criteria → You can’t compare models</li>
        <li>Testing multiple things at once → Hard to isolate issues</li>
      </ul>
      <h2>Example of a good eval prompting in a speech to text tool (STT)</h2>
      <h3>1️⃣ Eval case (what you run on your STT system)</h3>
      <pre>
        <code>{`{
          "id": "stt_eval_001",
          "audio_transcript_reference": "Good morning, this is Sarah Jones from the Ministry of Justice calling about case number 48291. Please call me back by 3 PM.",
          "model_output": "<TRANSCRIPT GENERATED BY STT TOOL>",
          "evaluation_method": "llm_judge"
        }`}
      </code>
    </pre>

      <h3>2️⃣ LLM judge prompt (how you judge the model’s output)</h3>
      <pre>
        <code>{`

        {
          "id": "stt_eval_001",

          "evaluation_type": "llm_judge",

          "judge_prompt": {
            "role": "system",
            "content": "You are evaluating the quality of a speech-to-text transcription."
          },

          "task": {
            "objective": "Evaluate how accurately and clearly the model transcript matches the reference transcript."
          },

          "rubric": {
            "accuracy": {
              "range": "0-3",
              "criteria": {
                "3": "All words and details correct",
                "2": "Minor errors that do not change meaning",
                "1": "Errors that affect meaning",
                "0": "Major errors or incorrect transcription"
              }
            },
            "completeness": {
              "range": "0-2",
              "criteria": {
                "2": "All content present",
                "1": "Minor omissions",
                "0": "Significant missing content"
              }
            },
            "formatting": {
              "range": "0-1",
              "criteria": {
                "1": "Proper punctuation, casing, readable",
                "0": "Poor formatting or hard to read"
              }
            }
          },

          "instructions": [
            "Be strict but fair",
            "Do not infer missing information",
            "Score only based on the provided text"
          ],

          "inputs": {
            "reference_transcript": "Good morning, this is Sarah Jones from the Ministry of Justice calling about case number 48291. Please call me back by 3 PM.",
            "model_transcript": "<MODEL OUTPUT HERE>"
          },

          "output_format": {
            "type": "json",
            "schema": {
              "accuracy": "integer (0-3)",
              "completeness": "integer (0-2)",
              "formatting": "integer (0-1)",
              "total_score": "integer (sum of above)",
              "justification": "string (max 50 words)"
            },
            "constraints": [
              "Return only valid JSON",
              "Use exactly these keys",
              "Do not include extra text",
              "total_score must equal sum of scores"
            ]
          }
        }
        
        
        `}</code>
      </pre>

      <h3>Picking a LLM judge model guidence</h3>
      <ul>
        <li>Use a strong reasoning model</li>
        <li>Pick one at least as capable as the model being evaluated</li>
        <li>Ensure reliable instruction-following</li>
        <li>Aim for consistent, low-variance outputs</li>
      </ul>
    </div>
  );
}
